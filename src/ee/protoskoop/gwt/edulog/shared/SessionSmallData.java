package ee.protoskoop.gwt.edulog.shared;

import java.util.HashMap;

public class SessionSmallData {
	
	private HashMap sessionSmallData;
	private String course;
	private String activity;
	
	public HashMap getSessionSmallData() {
		return sessionSmallData;
	}
	public void setSessionSmallData(HashMap sessionSmallData) {
		this.sessionSmallData = sessionSmallData;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public String getActivity() {
		return activity;
	}
	public void setActivity(String activity) {
		this.activity = activity;
	}

}
